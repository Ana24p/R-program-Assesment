# Define two integer vectors
vec1 <- c(10L, 20L, 30L, 40L)
vec2 <- c(2L, 4L, 5L, 8L)

# Addition
add_result <- vec1 + vec2

# Multiplication
mul_result <- vec1 * vec2

# Division
div_result <- vec1 / vec2

# Print results
cat("Vector 1:", vec1, "\n")
cat("Vector 2:", vec2, "\n\n")

cat("Addition Result:", add_result, "\n")
cat("Multiplication Result:", mul_result, "\n")
cat("Division Result:", div_result, "\n")
